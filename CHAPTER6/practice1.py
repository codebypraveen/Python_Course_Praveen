# Write a Python program to print numbers from 1 to 10 using a while loop.

i =1

while i<=10:
    print(i)
    i+=1

print("now we are out while loop, Question 1 Complete")  

# Write a program to print numbers from 10 down to 1 using a while loop.

j= 10
while j>=1:
    print(j)
    j-=1
    print("j =",j)
print("Now we are out of loop, Question 2 Complete")    