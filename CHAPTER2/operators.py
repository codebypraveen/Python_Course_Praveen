# arithmetic operator
x = 10
y = 5

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print(x**y)

# Comparision operator

print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)

# Logical operator

print("AND operator result : ", x==y and x!=y)
print("OR operator result : ", x>y or x<y)
print("NOT operator result : ",not (x>y))




